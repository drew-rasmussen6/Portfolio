"""## 8. Discount Impact Analysis
Are discounts driving volume? What's the tradeoff on margins?
"""

discounts = sql("""
    SELECT
        CASE
            WHEN discount_pct = 0   THEN '0% (None)'
            WHEN discount_pct <= 15 THEN '1-15%'
            WHEN discount_pct <= 20 THEN '16-20%'
            ELSE '21%+'
        END AS discount_tier,
        COUNT(order_id)                                      AS orders,
        ROUND(SUM(total_revenue), 2)                         AS revenue,
        ROUND(SUM(profit) * 100.0 / SUM(total_revenue), 1)  AS margin_pct,
        ROUND(AVG(total_revenue), 2)                         AS avg_order_value
    FROM orders
    WHERE status = 'Completed'
    GROUP BY discount_tier
    ORDER BY discount_tier
""")

fig = make_subplots(
    rows=1, cols=2,
    subplot_titles=("Order Volume by Discount Tier", "Profit Margin by Discount Tier")
)

fig.add_trace(go.Bar(
    x=discounts["discount_tier"], y=discounts["orders"],
    marker_color="#f59e0b", text=discounts["orders"], textposition="outside",
    name="Orders"
), row=1, col=1)

fig.add_trace(go.Bar(
    x=discounts["discount_tier"], y=discounts["margin_pct"],
    marker_color="#10b981", text=discounts["margin_pct"].apply(lambda x: f"{x}%"),
    textposition="outside", name="Margin %"
), row=1, col=2)

fig.update_layout(height=400, template="plotly_dark", showlegend=False,
                  title_text="Discount Impact: Volume vs Margins")
fig.show()

print("\nDiscount summary table:")
discounts

"""## 9. Order Status Breakdown
What percentage of orders are completed vs returned vs cancelled?
"""

status = sql("""
    SELECT
        status,
        COUNT(order_id)                AS orders,
        ROUND(SUM(total_revenue), 2)   AS revenue,
        ROUND(COUNT(order_id) * 100.0 / (SELECT COUNT(*) FROM orders), 1) AS pct_of_total
    FROM orders
    GROUP BY status
    ORDER BY orders DESC
""")

status_colors = {"Completed": "#10b981", "Returned": "#f59e0b", "Cancelled": "#ef4444"}

fig = px.bar(
    status, x="status", y="orders",
    color="status", color_discrete_map=status_colors,
    title="Orders by Status", text="orders",
    labels={"orders": "Number of Orders", "status": ""}
)
fig.update_traces(textposition="outside")
fig.update_layout(height=380, template="plotly_dark", showlegend=False)
