import sqlite3
import pandas as pd
import numpy as np
import random
from datetime import datetime, timedelta
import plotly.express as px
import plotly.graph_objects as go
from plotly.subplots import make_subplots
import warnings
warnings.filterwarnings('ignore')

random.seed(42)
np.random.seed(42)

print("Libraries loaded")

# synthetic data

NUM_CUSTOMERS = 500
NUM_ORDERS = 5000
START_DATE = datetime(2024, 1, 1)
END_DATE = datetime(2025, 12, 31)

PRODUCTS = {
    "Wireless Earbuds":   {"category": "Electronics", "base_price": 59.99,  "cost": 22.00},
    "Phone Case":         {"category": "Electronics", "base_price": 19.99,  "cost": 4.50},
    "USB-C Cable":        {"category": "Electronics", "base_price": 12.99,  "cost": 2.80},
    "Bluetooth Speaker":  {"category": "Electronics", "base_price": 89.99,  "cost": 35.00},
    "Laptop Stand":       {"category": "Electronics", "base_price": 44.99,  "cost": 15.00},
    "Running Shoes":      {"category": "Apparel",     "base_price": 119.99, "cost": 45.00},
    "Hoodie":             {"category": "Apparel",     "base_price": 49.99,  "cost": 18.00},
    "Baseball Cap":       {"category": "Apparel",     "base_price": 24.99,  "cost": 7.00},
    "Yoga Mat":           {"category": "Fitness",     "base_price": 34.99,  "cost": 10.00},
    "Water Bottle":       {"category": "Fitness",     "base_price": 22.99,  "cost": 5.50},
    "Resistance Bands":   {"category": "Fitness",     "base_price": 14.99,  "cost": 3.00},
    "Desk Lamp":          {"category": "Home",        "base_price": 39.99,  "cost": 14.00},
    "Scented Candle":     {"category": "Home",        "base_price": 16.99,  "cost": 4.00},
    "Throw Blanket":      {"category": "Home",        "base_price": 29.99,  "cost": 9.00},
    "Coffee Mug":         {"category": "Home",        "base_price": 14.99,  "cost": 3.50},
}

REGIONS = ["Northeast", "Southeast", "Midwest", "Southwest", "West"]
CHANNELS = ["Website", "Mobile App", "Social Media", "Email Campaign"]
STATUSES = ["Completed", "Completed", "Completed", "Completed", "Returned", "Cancelled"]

FIRST_NAMES = ["Emma", "Liam", "Olivia", "Noah", "Ava", "Ethan", "Sophia", "Mason",
               "Isabella", "James", "Mia", "Logan", "Charlotte", "Lucas", "Amelia"]
LAST_NAMES = ["Smith", "Johnson", "Williams", "Brown", "Jones", "Garcia", "Miller",
              "Davis", "Rodriguez", "Martinez", "Wilson", "Anderson", "Thomas", "Lee"]

# --- Generate customers ---
customers = []
for i in range(1, NUM_CUSTOMERS + 1):
    customers.append({
        "customer_id": i,
        "name": f"{random.choice(FIRST_NAMES)} {random.choice(LAST_NAMES)}",
        "email": f"customer{i}@email.com",
        "region": random.choice(REGIONS),
        "signup_date": (START_DATE + timedelta(days=random.randint(0, 365))).strftime("%Y-%m-%d"),
    })

# Generate orders
orders = []
product_names = list(PRODUCTS.keys())

for i in range(1, NUM_ORDERS + 1):
    customer = random.choice(customers)
    day_offset = random.randint(0, (END_DATE - START_DATE).days)
    order_date = START_DATE + timedelta(days=day_offset)

    # Seasonal boost for holidays
    if order_date.month in [11, 12] and random.random() < 0.3:
        target_month = random.choice([11, 12])
        # Determine the maximum valid day for the target month
        max_day = 30 if target_month == 11 else 31 # November has 30 days, December has 31

        # Ensure the day is not out of range for the new month
        new_day = min(order_date.day, max_day)

        order_date = order_date.replace(month=target_month, day=new_day)

    product_name = random.choice(product_names)
    product = PRODUCTS[product_name]
    quantity = random.choices([1, 2, 3, 4, 5], weights=[50, 25, 15, 7, 3])[0]
    discount = random.choices([0, 0.1, 0.15, 0.2, 0.25], weights=[60, 15, 10, 10, 5])[0]
    unit_price = round(product["base_price"] * (1 - discount), 2)
    total = round(unit_price * quantity, 2)
    cost = round(product["cost"] * quantity, 2)

    orders.append({
        "order_id": i,
        "customer_id": customer["customer_id"],
        "order_date": order_date.strftime("%Y-%m-%d"),
        "product": product_name,
        "category": product["category"],
        "quantity": quantity,
        "unit_price": unit_price,
        "total_revenue": total,
        "total_cost": cost,
        "profit": round(total - cost, 2),
        "discount_pct": int(discount * 100),
        "channel": random.choice(CHANNELS),
        "status": random.choice(STATUSES),
        "region": customer["region"],
    })

df_customers = pd.DataFrame(customers)
df_orders = pd.DataFrame(orders)

# Load into SQLite
conn = sqlite3.connect(":memory:")  # In-memory database (no files needed)
df_customers.to_sql("customers", conn, if_exists="replace", index=False)
df_orders.to_sql("orders", conn, if_exists="replace", index=False)

# Helper to run queries
def sql(query):
    return pd.read_sql_query(query, conn)

print(f"Database ready")
print(f"  - customers table: {len(df_customers)} rows")
print(f"  - orders table:    {len(df_orders)} rows")
print(f"  - Date range: {START_DATE.date()} → {END_DATE.date()}")
print(f"  - Products: {len(PRODUCTS)} across {len(set(p['category'] for p in PRODUCTS.values()))} categories")

